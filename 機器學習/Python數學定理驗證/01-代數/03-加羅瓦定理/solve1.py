import sympy as sp

# 定義一個多項式
x = sp.symbols('x')
polynomial = x**3 - 6*x**2 + 11*x - 6  # 這是一個三次方程，根為1, 2, 3

# 計算多項式的根
roots = sp.solve(polynomial, x)
print("多項式的根:", roots)

# 計算加羅瓦群（這裡簡單化，實際中加羅瓦群的構造較為複雜）
# 通常使用群論工具來計算加羅瓦群，但這裡可以檢查對稱性
# 這個例子展示了多項式的解的對稱性

# 檢查多項式是否有可解根式解
if len(roots) == 3:
    print("此多項式的解是可以表示為根式解的。")
else:
    print("此多項式的解不可用根式表示。")

